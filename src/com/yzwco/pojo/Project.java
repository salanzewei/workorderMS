package com.yzwco.pojo;

public class Project {
    private int id;
    private String projectName;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getProjectName() {
        return projectName;
    }

    public void setProjectName(String project_name) {
        this.projectName = project_name;
    }

}
